#make a chart
def make_chart(title, x_label, y_label, x_replacement, data, int_float = 'int'):
    graph = {'x':[], 'y':[]}
    for d in data:
        #parse label
        val = d['label']
        val = val.replace(x_replacement,'')
        if int_float == 'int':
            val = int(val)
        else:
            val = float(val)
        graph['x'].append(val)
        val = d['scores']['accuracy']
        graph['y'].append(val)
    print(graph)
    plt.title(title)
    plt.plot(graph['x'], graph['y'])
    plt.xlabel(x_label)
    plt.ylabel(y_label)
    plt.show()
    


